public class ConditionalDemo {

	public static void main(String[] args) {

		int marks = 90;
		int pack = 5 ;

		if (marks >= 40 && pack >= 15 && true) {
			System.out.println("good");
		} else if (marks >= 20 || pack <= 10 || false) {
			System.out.println("avg");
		} 

	}

}



